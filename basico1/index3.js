// condição switch - estrutura de controle com opções pré definidas
let cor = "amarelo"

switch (cor){

    case "verde":
        console.log("siga");
        break;

    case "amarelo":
        console.log("atencao");
        break;

    case "vermelho":
        console.log("pare");
        break;

    default:
        console.log("nao sei")
}
